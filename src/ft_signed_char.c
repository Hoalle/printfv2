#include "../inc/ft_printf.h"

int			ft_signed_char(int n)
{
	while (n < -128)
	{
		n = 128 - ((n + 128) * (-1));
		if (n > -128)
			return (n);
	}
	while (n > 127)
	{
		n = (128 - (n - 128)) * (-1);
		if (n < 127)
			return (n);
	}
	return (n);
}
